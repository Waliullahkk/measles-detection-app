import React from 'react';
import { Card, Alert, Button, Badge } from 'react-bootstrap';
import { FaCheckCircle, FaTimesCircle, FaExclamationTriangle, FaDownload } from 'react-icons/fa';
import '../styles/Results.css';

const Results = ({ results, onBack }) => {
  if (!results) {
    return null;
  }

  const {
    success,
    final_prediction,
    final_confidence,
    model_confidence,
    symptoms_provided,
    symptom_count,
    recommendation,
    risk_level,
  } = results;

  const getRiskColor = (level) => {
    switch (level) {
      case 'CRITICAL':
        return 'danger';
      case 'HIGH':
        return 'warning';
      case 'MODERATE':
        return 'info';
      case 'LOW':
        return 'success';
      default:
        return 'secondary';
    }
  };

  const getPredictionIcon = (prediction) => {
    return prediction === 'measles' ? (
      <FaExclamationTriangle className="me-2 text-danger" />
    ) : (
      <FaCheckCircle className="me-2 text-success" />
    );
  };

  const downloadReport = () => {
    const reportContent = `
MEASLES DETECTION ANALYSIS REPORT
==================================
Generated: ${new Date().toLocaleString()}

PREDICTION RESULTS:
-------------------
Final Diagnosis: ${final_prediction.toUpperCase()}
Confidence Level: ${(final_confidence * 100).toFixed(2)}%
Risk Level: ${risk_level}
Model Confidence: ${(model_confidence * 100).toFixed(2)}%

SYMPTOMS:
---------
Number of Symptoms: ${symptom_count}
Symptoms Detected: ${symptoms_provided.join(', ') || 'None'}

RECOMMENDATION:
---------------
Risk Level: ${recommendation.level}
Recommendation: ${recommendation.recommendation}

Actions to Take:
${recommendation.actions.map((action, idx) => `${idx + 1}. ${action}`).join('\n')}

DISCLAIMER:
-----------
This analysis is for informational purposes only.
Please consult with a qualified healthcare professional for diagnosis and treatment.

========================================
Report Generated by Measles Detection System
    `;

    const element = document.createElement('a');
    element.setAttribute('href', 'data:text/plain;charset=utf-8,' + encodeURIComponent(reportContent));
    element.setAttribute('download', `Measles_Report_${new Date().getTime()}.txt`);
    element.style.display = 'none';
    document.body.appendChild(element);
    element.click();
    document.body.removeChild(element);
  };

  return (
    <div className="results-container">
      <Card className="shadow mb-4 results-card">
        <Card.Header className={`bg-${getRiskColor(risk_level)} text-white`}>
          <h4>
            {getPredictionIcon(final_prediction)}
            Analysis Results
          </h4>
          <Badge bg={getRiskColor(risk_level)} className="mt-2">
            {risk_level} RISK
          </Badge>
        </Card.Header>

        <Card.Body>
          {/* Main Prediction */}
          <div className="prediction-section mb-4 p-3 rounded bg-light">
            <h5 className="mb-3">Primary Diagnosis</h5>
            <div className="row">
              <div className="col-md-6">
                <p className="mb-2">
                  <strong>Prediction:</strong>
                </p>
                <h6 className={`text-${final_prediction === 'measles' ? 'danger' : 'success'}`}>
                  {final_prediction.toUpperCase()}
                </h6>
              </div>
              <div className="col-md-6">
                <p className="mb-2">
                  <strong>Confidence:</strong>
                </p>
                <h6>{(final_confidence * 100).toFixed(2)}%</h6>
                <div className="progress" style={{ height: '20px' }}>
                  <div
                    className={`progress-bar bg-${final_prediction === 'measles' ? 'danger' : 'success'}`}
                    style={{ width: `${Math.min(final_confidence * 100, 100)}%` }}
                  />
                </div>
              </div>
            </div>
          </div>

          {/* Symptoms Analysis */}
          <div className="symptoms-section mb-4 p-3 rounded bg-light">
            <h5 className="mb-3">Symptoms Analysis</h5>
            <p>
              <strong>Symptoms Detected:</strong> {symptom_count} symptoms
            </p>
            {symptoms_provided.length > 0 ? (
              <div>
                {symptoms_provided.map((symptom, idx) => (
                  <Badge key={idx} bg="info" className="me-2 mb-2">
                    {symptom}
                  </Badge>
                ))}
              </div>
            ) : (
              <p className="text-muted">No symptoms selected</p>
            )}
          </div>

          {/* Recommendation */}
          <Alert variant={getRiskColor(risk_level)} className="mt-4">
            <h6 className="mb-2">
              {recommendation.level === 'HIGH RISK' ||
              recommendation.level === 'CRITICAL' ? (
                <FaExclamationTriangle className="me-2" />
              ) : (
                <FaCheckCircle className="me-2" />
              )}
              {recommendation.level}
            </h6>
            <p className="mb-3">{recommendation.recommendation}</p>
            <strong>Recommended Actions:</strong>
            <ul className="mt-2 mb-0">
              {recommendation.actions.map((action, idx) => (
                <li key={idx}>{action}</li>
              ))}
            </ul>
          </Alert>

          {/* Model Confidence Info */}
          <div className="model-info mt-4 p-3 rounded bg-secondary bg-opacity-10">
            <h6>Additional Information</h6>
            <p className="mb-1">
              <small>
                <strong>Model Confidence:</strong> {(model_confidence * 100).toFixed(2)}%
              </small>
            </p>
            <p className="mb-0">
              <small className="text-muted">
                This analysis combines image analysis (70%) with symptom information (30%)
              </small>
            </p>
          </div>

          {/* Action Buttons */}
          <div className="d-grid gap-2 mt-4">
            <Button
              variant="info"
              onClick={downloadReport}
              className="mb-2"
            >
              <FaDownload className="me-2" />
              Download Report
            </Button>
            <Button
              variant="primary"
              onClick={onBack}
            >
              New Analysis
            </Button>
          </div>

          {/* Disclaimer */}
          <Alert variant="light" className="mt-4 small text-muted">
            <strong>⚠️ Medical Disclaimer:</strong> This analysis is for informational purposes
            only. It is not a substitute for professional medical advice, diagnosis, or
            treatment. Always consult with a qualified healthcare provider.
          </Alert>
        </Card.Body>
      </Card>
    </div>
  );
};

export default Results;
